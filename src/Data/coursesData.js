// Export functions for accessing course data
export const getAllSchools = () => {
  return Object.keys(coursesData);
};

export const getSchoolCourses = (school) => {
  return coursesData[school] || {};
};

export const getCourseData = (school, course) => {
  return coursesData[school]?.[course] || null;
};

export const getAllCourses = () => {
  const allCourses = [];
  
  Object.entries(coursesData).forEach(([schoolName, courses]) => {
    Object.entries(courses).forEach(([courseSlug, courseData]) => {
      allCourses.push({
        ...courseData,
        school: schoolName,
        slug: courseSlug
      });
    });
  });
  
  return allCourses;
};

// Get courses by school name
export const getCoursesBySchool = (schoolName) => {
  const school = coursesData[schoolName];
  if (!school) return [];
  
  return Object.entries(school).map(([slug, courseData]) => ({
    ...courseData,
    school: schoolName,
    slug
  }));
};

// Search courses by term
export const searchCourses = (searchTerm) => {
  const allCourses = getAllCourses();
  const term = searchTerm.toLowerCase();
  
  return allCourses.filter(course => 
    course.title.toLowerCase().includes(term) ||
    course.description.toLowerCase().includes(term) ||
    course.school.toLowerCase().includes(term) ||
    (course.highlights && course.highlights.some(h => h.toLowerCase().includes(term)))
  );
};

// Get popular/featured courses
export const getFeaturedCourses = () => {
  return [
    getCourseData('engineering', 'computer-science'),
    getCourseData('management', 'mba'),
    getCourseData('biotechnology', 'bsc-biotechnology'),
    getCourseData('law', 'ballb')
  ].filter(Boolean).map((course, index) => {
    const schools = ['engineering', 'management', 'biotechnology', 'law'];
    const slugs = ['computer-science', 'mba', 'bsc-biotechnology', 'ballb'];
    return {
      ...course,
      school: schools[index],
      slug: slugs[index]
    };
  });
};
